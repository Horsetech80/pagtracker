'use client';

import * as React from 'react';
import { ThemeProvider as NextThemesProvider } from 'next-themes';
import { type ThemeProviderProps } from 'next-themes';

// COMPONENTE ISOLADO PARA PAINEL ADMINISTRATIVO
// Este componente é uma cópia isolada do theme provider
// Mantém a mesma funcionalidade mas evita conflitos entre painéis

export function AdminThemeProvider({ children, ...props }: ThemeProviderProps) {
  return (
    <NextThemesProvider
      attribute="class"
      defaultTheme="system"
      enableSystem
      disableTransitionOnChange
      {...props}
    >
      {children}
    </NextThemesProvider>
  );
}